    public String leer(String nombre) throws FileNotFoundException, IOException

    //El parametro nombre indica el nombre del archivo por ejemplo "prueba.txt" 
    {
        String strLinea;
        try{

            FileInputStream fstream = new FileInputStream(nombre);
            // Creamos el Buffer de Lectura
            try ( // Creamos el objeto de entrada
                    DataInputStream entrada = new DataInputStream(fstream)) {
                // Creamos el Buffer de Lectura
                BufferedReader buffer = new BufferedReader(new InputStreamReader(entrada));
                
                // Leer el archivo linea por linea
                while ((strLinea = buffer.readLine()) != null)   {
                    // Imprimimos la línea por pantalla
                    System.out.println (strLinea);
                }
            }

        return strLinea;

        }catch(IOException e){
            System.out.println("Error:"+e.getMessage());
        }
        return null;
    }
    
