    public boolean yaingreso(String ci){
        try{
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            boolean b =helper.getRegistrosexiste(ci);
            helper.cerrarSesion();
            
            return b;
        }catch(Exception io){
            JOptionPane.showMessageDialog(null, "Error en Conexion");
        }
        return false;
    }
    
