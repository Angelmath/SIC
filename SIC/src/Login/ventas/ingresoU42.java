    public boolean validacion(){
        
        if(getjTextField8().getText().equalsIgnoreCase("")){
            JOptionPane.showConfirmDialog(null, "Razon del Proveedor no Ingresado", "Info", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE);
            return false;
        }
        if(getjTextField9().getText().equalsIgnoreCase("")){
            JOptionPane.showConfirmDialog(null, "RUC del Proveedor no Ingresado", "Info", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE);
            return false;
        }
        
        
    return true;
    }
    
    
