    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(validacion()){
        if(cls==null){
        Proveedores nuevo = new Proveedores();
        nuevo.setRazon(getjTextField8().getText());
        nuevo.setNumero(getjTextField3().getText());
        nuevo.setContacto(getjTextField2().getText());
        nuevo.setRuc(getjTextField9().getText());
        nuevo.setCorreo(getjTextField4().getText());
        nuevo.setDireccion(getjTextField5().getText());
             
        try {
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            helper.crearOrActualizarObjeto(nuevo);
            helper.confirmarTransaccion();
            helper.cerrarSesion();
        } catch (Exception ex) {
            Logger.getLogger(marco.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else{

            try {
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            cls.setRazon(getjTextField8().getText());
            cls.setNumero(getjTextField3().getText());
            cls.setContacto(getjTextField2().getText());
            cls.setRuc(getjTextField9().getText());
            cls.setCorreo(getjTextField4().getText());
            cls.setDireccion(getjTextField5().getText());
            helper.actualizarObjeto(cls);
            helper.confirmarTransaccion();
            helper.cerrarSesion();
            } catch (Exception ex) {
                Logger.getLogger(marco.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(null, "Proveedor Agregado");
        dispose(); 
        }
    }//GEN-LAST:event_jButton1MouseClicked

