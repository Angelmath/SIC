    public void actualizar(){
        try{
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            List<Contactos> list = helper.getContactos(""+idcl);
            helper.cerrarSesion();
            
            DefaultTableModel temp4 = (DefaultTableModel) getjTable2().getModel();
            for(int i=temp4.getRowCount()-1;i>=0;i--){
                temp4.removeRow(i);
            }
            if(list!=null){
                int i=0;
                DefaultTableModel temp = (DefaultTableModel) getjTable2().getModel();
                for(Contactos x: list){
                    String c1=x.getNombre();
                    String c2=x.getCargo();
                    String c3=x.getCorreo();
                    String c4=x.getTelefono();
                    String c5=x.getObservaciones();
                    Object nuevo[]= {c1,c2,c3,c4,c5,x.getId()};
                    temp.addRow(nuevo);
                }
            }
        }catch(Exception io){
        
        }
    }
    
