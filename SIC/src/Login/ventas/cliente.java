        int row = jTable2.getSelectedRow();
        int col = jTable2.getSelectedColumn();
        int num = Integer.parseInt(jTable2.getValueAt(row, 5).toString());
        try{
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            conc= (Contactos)helper.obtenerObjeto(Contactos.class, num);
            helper.cerrarSesion();
        }catch(Exception io){}
        if(conc!=null){
            ingresoContactos in = new ingresoContactos(null, true, null, conc,""+idcl);
            actualizar();
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un Contacto");
        }
