    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        int col = jTable1.getSelectedColumn();
        if(col==12){
            String fnum = jTable1.getValueAt(row, 2).toString();
            String idret = jTable1.getValueAt(row, 12).toString();
            String idctecobrar = jTable1.getValueAt(row, 0).toString();
            Double d = Double.parseDouble(jTable1.getValueAt(row, 7).toString().replace("$",""));
            Retencionfactu retencion=null;
            try{
                ServiceHb helper = new ServiceHb();
                helper.iniciarTransaccion();
                if(!idret.equalsIgnoreCase("")){
                    retencion=helper.getrentencion(idret);
                }
                helper.cerrarSesion();
            }catch(Exception io){}
            
            if(retencion!=null){
                retencionctecobrar retencion1 = new retencionctecobrar(null, null, true,retencion,idctecobrar,d);
            }else{
                retencionctecobrar retencion1 = new retencionctecobrar(null, null, true,null,idctecobrar,d);
            }
            llenar();
        }
        if(col==16){
            try{
                ServiceHb helper = new ServiceHb();
                helper.iniciarTransaccion();
                Ctecobrar ce = (Ctecobrar)helper.obtenerObjeto(Ctecobrar.class, Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
                ce.setFactura(jTable1.getValueAt(row, 2).toString());
                ce.setCliente(jTable1.getValueAt(row, 3).toString());
                ce.setAutfactura(jTable1.getValueAt(row, 4).toString());
                ce.setPlanpagos(jTable1.getValueAt(row, 5).toString());
                ce.setObservacion(jTable1.getValueAt(row, 6).toString());
                ce.setSubmontos(jTable1.getValueAt(row, 7).toString().replace("$","").replace(",","."));
                ce.setIvamontos(jTable1.getValueAt(row, 8).toString().replace("$","").replace(",","."));
                ce.setMontos(jTable1.getValueAt(row, 9).toString().replace("$","").replace(",","."));
                ce.setSaldo(jTable1.getValueAt(row, 14).toString().replace("$","").replace(",","."));
                ce.setTotalretencion(jTable1.getValueAt(row, 10).toString().replace("$","").replace(",","."));
                ce.setNumretencion(jTable1.getValueAt(row, 11).toString());
                
                String[] cadena = jTable1.getValueAt(row, 1).toString().split("-");
                Calendar ca = Calendar.getInstance();
                ca.set(Integer.parseInt(cadena[0]), Integer.parseInt(cadena[1])-1, Integer.parseInt(cadena[2]));
                ce.setFecha( new java.sql.Date(ca.getTime().getTime()));
                
                helper.actualizarObjeto(ce);
                helper.confirmarTransaccion();
                helper.cerrarSesion();
                JOptionPane.showMessageDialog(null, "Editado");
            }catch(Exception io){}
            dispose();
        }
        if(col==17){
            try{
                ServiceHb helper = new ServiceHb();
                helper.iniciarTransaccion();
                Ctecobrar ce = (Ctecobrar)jTable1.getValueAt(row, 16);
                helper.eliminarObjeto(ce);
                helper.confirmarTransaccion();
                helper.cerrarSesion();
                JOptionPane.showMessageDialog(null, "Editado");
            }catch(Exception io){}
        }
    }//GEN-LAST:event_jTable1MouseClicked

