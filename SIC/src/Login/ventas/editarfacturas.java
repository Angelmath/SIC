    public void llenar(){
        try {
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            ltcc = helper.getListCteCobrar(cobrar);
            helper.cerrarSesion();        
        } catch (Exception io) {
        }
        reiniciarJTable(jTable1);
        if(ltcc!=null){
            for(Ctecobrar i: ltcc){
                float sub = 0;
                float iva = 0;
                float val = 0;
                float val2 = 0;
                float rett = 0;
                if(i.getSubmontos()!=null){
                    sub = Float.parseFloat(i.getSubmontos());
                }
                if(i.getIvamontos()!=null){
                    iva = Float.parseFloat(i.getIvamontos());
                }
                if(i.getMontos()!=null){
                    val = Float.parseFloat(i.getMontos());
                }
                if(i.getSaldo()!=null){
                    val2 = Float.parseFloat(i.getSaldo());
                }   
                if(i.getTotalretencion()!=null){
                    rett = Float.parseFloat(i.getTotalretencion());
                }
                float totalfact= val-rett;
                DefaultTableModel temp = (DefaultTableModel) jTable1.getModel();
                Object nuevo[]= {i.getId(),i.getFecha(),i.getFactura(),i.getCliente(),i.getAutfactura(),i.getPlanpagos(),i.getObservacion(),"$"+df.format(sub).replace(",", "."),"$"+df.format(iva).replace(",", "."),"$"+df.format(val).replace(",", "."),"$"+df.format(rett).replace(",", "."),i.getNumretencion(),i.getIdretencion(),"$"+df.format(totalfact).replace(",", "."),"$"+df.format(val2).replace(",", "."),"$"+df.format(val-val2-rett).replace(",", "."),i,i};
                temp.addRow(nuevo);
            }
        }
    }
