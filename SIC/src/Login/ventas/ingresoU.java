    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(!getjTextField2().getText().isEmpty()){
        Clientes nuevo = new Clientes();
        nuevo.setCi(getjTextField8().getText());
        nuevo.setNombre(getjTextField3().getText());
        nuevo.setApellido(getjTextField2().getText());
        nuevo.setRuc(getjTextField8().getText());
        nuevo.setPersona(getjComboBox1().getSelectedItem().toString());
        nuevo.setNumero1(getjTextField5().getText());
        nuevo.setNumero2(getjTextField5().getText());
        nuevo.setLogin(marc.Usuario);
        nuevo.setCorreo(getjTextField4().getText());
        nuevo.setLogin2(l2);
        nuevo.setLogin3(l3);
        nuevo.setUsuariotipo2(l2.getNombre());
        nuevo.setUsuariotipo2(l3.getNombre());
        nuevo.setResponsable(marc.Usuario.getNombre());
        
        try {
            ServiceHb helper = new ServiceHb();
            helper.iniciarTransaccion();
            helper.crearOrActualizarObjeto(nuevo);
            helper.confirmarTransaccion();
            helper.cerrarSesion();
        } catch (Exception ex) {
            Logger.getLogger(marco.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Cliente Agregado");
        dispose(); 
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese Razon Social");
        }
    }//GEN-LAST:event_jButton1MouseClicked

